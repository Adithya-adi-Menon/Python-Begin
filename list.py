#Lists - Have Brackets []

movies = ["Movie 1","Movie 2", "Movie 3", "Movie 4"]

print(movies[0]) #Count Starts with zero 
print(movies[1:4]) # 1-3
print(movies[1:]) #
print(movies[:1])#Prints before count 1
print(movies[-1])# grab last item
print(len(movies))

movies.append("Movie 5") #to add item to the list

print(movies)

movies.pop() #to delete items from the list

print(movies)

movies.pop(0) 

print(movies)


